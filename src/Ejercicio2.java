
public class Ejercicio2 {

	public static void main(String[] args) {
	     for (int i = 6; i >= 1; i--) {//el valor de i dice cuantos asteriscos se deben de imprimir
	            for (int a = 1; a <= i; a++) {
	                System.out.print("* ");//esto sirve para que todo se imprima en una sola linea
	            }
	            System.out.println();//esto es para que de un salto de fila
	        }
	    }
	}